#include<stdio.h>

int main(){
	int num1 = 20;
	int num2 = 10;
	int tong = num1 + num2;
	int hieu = num1 - num2;
	int tich = num1 * num2;
	int thuong = num1 / num2;
	printf("Tong cua %d va %d la: %d\n",num1, num2, tong);
	printf("Hieu cua %d va %d la: %d\n",num1, num2, hieu);
	printf("Tich cua %d va %d la: %d\n",num1, num2, tich);
	printf("Thuong cua %d va %d la: %d\n",num1, num2, thuong);
	
	return 0;
}
